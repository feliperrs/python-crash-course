#? Se o alienígena for verde, exiba uma afirmação de que o jogador acabou de ganhar 5 pontos por abater o alienígena.

#? Se o alienígena não for verde, exiba uma afirmação de que o jogador acabou de ganhar 10 pontos.

#? Escreva duas versões desse programa que executem o bloco if e que executem o bloco else.

